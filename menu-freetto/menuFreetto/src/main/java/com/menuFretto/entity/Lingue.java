package com.menuFretto.entity;

public class Lingue {
	
	String nome;
	String url;
	
	
	
	public Lingue(String nome, String url) {
		super();
		this.nome = nome;
		this.url = url;
	}
	public String getNome() {
		return nome;
	}
	public String getUrl() {
		return url;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	
	
	

}
